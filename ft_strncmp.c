int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				j;

	i = 0;
	j = 0;
	while (s1[i] && i < n)
	{
		j = j + s1[i];
		i++;
	}
	i = 0;
	while (s2[i] && i < n)
	{
		j = j - s2[i];
		i++;
	}
	return (j);
}
